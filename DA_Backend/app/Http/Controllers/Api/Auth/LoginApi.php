     * @return \Illuminate\Http\Response
     */
    public function login(Request $request)
    {
        $credentials = $request->only('phone', 'password');

    if (Auth::attempt($credentials)) {
        $user = Auth::user();
        $roles = $user->roles;
        return response()->json([
            'roles' => $roles
        ], 200);
    } else {
        return response()->json(['error' => 'Unauthorized'], 401);
    }
    }

